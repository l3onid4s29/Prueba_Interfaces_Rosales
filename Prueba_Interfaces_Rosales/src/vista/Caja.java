    private void insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertarActionPerformed
        // TODO add your handling code here:
        
        double saldo = Double.parseDouble(saldoInicial.getText());
        caja = new Ahorros(nombres.getText(), saldo);
        lista.add(caja);
        String Cadena = String.valueOf(caja.getCuenta());
        String[] datos = new String[3];
        datos[0] = Cadena;
        datos[1] = nombres.getText();
        datos[2] = saldoInicial.getText();
        modelo.addRow(datos);
        nombres.setText("");
        saldoInicial.setText("");
    }//GEN-LAST:event_insertarActionPerformed

